package de.uni.bamberg.wiai.minf.forflow.datagenerator.controller.datageneration.filltype;

/**
 * This enum is used to set the type of interval. That could be either an integer
 * interval or either an double interval.
 * 
 * @author Michael Munz
 * @version 0.1
 * @since May/01/09
 */
public enum IntervalType
{
	/**
	 * specifies the interval as integer type values
	 */
	INTEGER,
	
	/**
	 * specifies the interval as double type values
	 */
	DOUBLE;
}